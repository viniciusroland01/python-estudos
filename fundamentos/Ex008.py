print(f'{n}Km para Dam é {n*100}dam.')
print(f'{n}Km para M é {n*1000}m.')
print(f'{n}Km para Dm é {n*10000}dm.')
print(f'{n}Km para Cm é {n*100000}cm.')
print(f'{n}Km para Mm é {n*1000000}mm.')
